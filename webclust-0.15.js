/**
    Returns the value of an input field as an integer array.
*/
function get_array(id, default_arr, conv_func=parseInt) {
    let e = document.getElementById(id);
    if (e == null) {
        return default_arr;
    }
    
    let str = e.value;
    
    let arr = str.split(",");
    for (let i in arr) {
        let val = arr[i];
        val = val.trim();
        arr[i] = conv_func(val);
        if (isNaN(arr[i])) {
            e.value = default_arr;
            return default_arr;
        }
    }
    return arr;
}

/**
    Returns the integer value from an input field.
*/
function get_value(id, default_val, min_val, max_val, conv_func=parseInt) {
    // Check if element is available
    let e = document.getElementById(id);
    if (e == null) {
        return default_val;
    }
    
    let str = e.value;
    str = str.trim();
    let val = conv_func(str);
    // Check if valid int
    if (isNaN(val)) {
        e.value = default_val;
        val = default_val;
    }

    // Range check
    if (val < min_val) {
        val = min_val;
        e.value = val;
    }
    if (val > max_val) {
        val = max_val;
        e.value = val;
    }

    return val;
}

/**
    Returns the selected value in a dropdown list.
*/
function get_select_list(id, default_val) {
    // Check if element is available
    let e = document.getElementById(id);
    if (e == null) {
        return default_val;
    }

    // Get selected value
    let val = e.options[e.selectedIndex].value;
    return val;
}


